public class RandomStuff {
    public static void main(String[] args) {
        int b = 0;
        for (int i = 0; i <= 3; i++){
            System.out.println("* * * * * * ==================================");
            System.out.println("* * * * * ==================================");
            b++;
        }
        if (b == 4){
            System.out.println("* * * * * * ==================================");
            System.out.println("===========================================");
        }
    }
}